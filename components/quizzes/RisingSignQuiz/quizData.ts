export const calculateRisingSign = (answers) => {
  const signCounts = {}

  // Count occurrences of each sign
  answers.forEach((answer) => {
    const sign = answer.value
    signCounts[sign] = (signCounts[sign] || 0) + 1
  })

  // Find the sign with the highest count
  let maxCount = 0
  let risingSign = 'Leo' // Default fallback

  Object.entries(signCounts).forEach(([sign, count]) => {
    if (count > maxCount) {
      maxCount = count
      risingSign = sign
    }
  })

  return risingSign
}
